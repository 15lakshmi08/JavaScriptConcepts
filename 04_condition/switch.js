const week = "sunday"

switch (week) {
    case "sunday":
        console.log("Sunday");
        break;
    case "monday":
        console.log("Monday");
        break;
    case "tuesday":
        console.log("Tuesday");
        break;
    case "wednesday":
        console.log("Wednesday");
        break;

    default:
        console.log("default case match");
        break;
}